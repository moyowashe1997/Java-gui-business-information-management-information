    private void RestockButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RestockButton5ActionPerformed
        if(found==true){
            boolean check=true;
            int index= StockNames.indexOf(NameTextField5.getText());
            int p =0;
            try{
             p= Integer.parseInt(RestockTextField8.getText());
        }catch(NumberFormatException e){
          showMessageDialog(null, "Restock Amount must be a number values", "Wrong data entered", ERROR_MESSAGE);
            check=false;
        }
        catch (NullPointerException e){
            showMessageDialog(null, "Quantity cannot be left open", "Data not entered", ERROR_MESSAGE);
            check=false;
                        }
            if(check!=false){
            StockQuantity.set(index,((StockQuantity.get(index)+p)));
            showMessageDialog(null, "Item Restocked");
            EditQuantityTextField7.setText(null);
            NameTextField5.setText(null);
            CPTextField6.setText(null);
                    }
        }else showMessageDialog(null, "Stock not found or searched for", "Stock not found", ERROR_MESSAGE);
    }//GEN-LAST:event_RestockButton5ActionPerformed


    private void AddButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButton1ActionPerformed
        String name = StockNameTextField1.getText();
        String quantity = QuantityTextField2.getText();
        String price = PriceTextField3.getText();
        int q=0;
        double p=0;
        boolean check=true;
        try{
            q= Integer.parseInt(quantity);
             p= Double.parseDouble(price);
        }catch(NumberFormatException e){
          showMessageDialog(null, "Quantity and Price must be number values", "Wrong data entered", ERROR_MESSAGE);
            StockNameTextField1.setText("");
            QuantityTextField2.setText("");
            QuantityTextField2.setText("");
            check=false;
        }
        catch (NullPointerException e){
            showMessageDialog(null, "Quantity and Price cannot be left open", "Data not entered", ERROR_MESSAGE);
            StockNameTextField1.setText("");
            QuantityTextField2.setText("");
            QuantityTextField2.setText("");
            check=false;
                        }
        if(check!=false){
        StockNames.add(name);
        StockPrices.add(p);
        StockQuantity.add(q);
        showMessageDialog(null,"New Stock Added");
        StockNameTextField1.setText(null);
        QuantityTextField2.setText(null);
        PriceTextField3.setText(null);
        }
    }//GEN-LAST:event_AddButton1ActionPerformed

    private void FindStockButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FindStockButton2ActionPerformed
        String name = NameTextField5.getText();
        found = StockNames.contains(name);
        if(found==true){
            int index = StockNames.indexOf(name);
            showMessageDialog(null,"Stock found");
            CPTextField6.setText(Double.toString(StockPrices.get(index)));
            EditQuantityTextField7.setText(Integer.toString(StockQuantity.get(index)));
        }else showMessageDialog(null,"Stock not found");
    }//GEN-LAST:event_FindStockButton2ActionPerformed

    private void ChangePriceButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangePriceButton3ActionPerformed
        if(found==true){
            boolean check=true;
            int index= StockNames.indexOf(NameTextField5.getText());
            double p =0;
            try{
             p= Double.parseDouble(NPTextField4.getText());
        }catch(NumberFormatException e){
          showMessageDialog(null, "New Price must be a number values", "Wrong data entered", ERROR_MESSAGE);
            check=false;
        }
        catch (NullPointerException e){
            showMessageDialog(null, "Price cannot be left open", "Data not entered", ERROR_MESSAGE);
            check=false;
                        }
            if(check!=false){
            StockPrices.set(index,p);
            showMessageDialog(null, "Price Changed");
            NPTextField4.setText(null);
            NameTextField5.setText(null);
            CPTextField6.setText(null);
                    }
        }else showMessageDialog(null, "Stock not found or searched for", "Stock not found", ERROR_MESSAGE);
    }//GEN-LAST:event_ChangePriceButton3ActionPerformed

